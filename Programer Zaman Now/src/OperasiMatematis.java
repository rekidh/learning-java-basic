public class OperasiMatematis {
    public static void main(String[] args) {
        //operator Matematika
        int pertama = 100;
        int kedua = 10;

        System.out.println(pertama-kedua);
        System.out.println(pertama+kedua);
        System.out.println(pertama/kedua);
        System.out.println(pertama*kedua);
        System.out.println(pertama%kedua);
    }
}
