package com.tutorial;

public class OperatorAssignments {
    public static void main(String[] args) {

        //Operator Assignment
        int a = 1;
        a= a+5;


        //assignments Operator jumlah
        int b = 1;
        b +=10; //b = b + 10;
        System.out.println("nilai a ="+b);

        //assignments Operator pengurangan
        int c = 100;
        c-=10; //b = b - 10;
        System.out.println("nilai a ="+c);

        //assignments Operator perkalian
        int d = 100;
        d*=10; //b = b * 10;
        System.out.println("nilai a ="+d);

        //assignments Operator pembagian
        int e = 100;
        e /= 10; //b = b / 10;
        System.out.println("nilai a ="+e);

        //assignments Operator modulus
        int f = 100;
        f %=10; //b = b % 10;
        System.out.println("nilai a ="+f);


    }
}
